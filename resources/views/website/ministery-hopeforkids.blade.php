    <p>Esperança Para Crianças é um treinamento do ministério de EE-Kids desenhado para ensinar às crianças como compartilharem a mensagem do Evangelho com outras pessoas e desenvolverem uma base sólida na sua fé.</p>
    <p>O programa geralmente é realizado em igrejas ou escolas cristãs e é liderado por adultos treinados no Workshop Esperança Para Crianças. Ele é projetado para crianças entre 8 e 12 anos, mas pode ser adaptado para crianças mais jovens ou mais velhas também.</p>
    <p>O programa é dividido em 12 sessões que abrangem diferentes aspectos da mensagem do Evangelho, como Salvação, Bíblia, Oração e Testemunho. O programa também inclui um componente de mentoria, onde as crianças são conduzidas por mentores/professores adultos que as ajudam a aplicar o que elas aprenderam e compartilhar a sua fé com outros.</p>
    <p>Em geral, Esperança Para Crianças é um programa do ministério de EE-Kids que ajuda as crianças a entenderem e compartilharem a mensagem do Evangelho de uma forma que seja relevante e significativa para elas.</p>

